# Assignment oprerator is used to assign a value to left hand side variable
# Example of Assignment operators in Python
# Operator	Example	Equivatent to
# =	       x = 5	x = 5
# +=	   x += 5	x = x + 5
# -=	   x -= 5	x = x - 5
# *=	   x *= 5	x = x * 5
# /=	   x /= 5	x = x / 5
# %=	   x %= 5	x = x % 5
# //=	   x //= 5	x = x // 5
# **=	   x **= 5	x = x ** 5
# &=	   x &= 5	x = x & 5
# |=	   x |= 5	x = x | 5
# ^=	   x ^= 5	x = x ^ 5
# >>=	  x >>= 5	x = x >> 5
# <<=	  x <<= 5	x = x << 5

x = 5

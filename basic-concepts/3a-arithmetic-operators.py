#!/usr/bin/python

x = 21
y = 5

# Output: x + y = 26
# Addition
print('x + y =',x+y)

# Output: x - y = 16
# Subtraction
print('x - y =',x-y)

# Output: x * y = 105
# Multiplication
print('x * y =',x*y)

# Output: x / y = 4.2
# Division, always results in float
print('x / y =',x/y)

# Output x % y = 1
# Modulo division, prints the remainder
print('x % y =', x%y)

# Output: x // y = 4
# Floor Division, whole number adjusted with left in the number line
print('x // y =',x//y)

# Output: x ** y = 4084101
# exponent x to the power y
print('x ** y =',x**y)

#!/usr/bin/python # Setting up the shebang

# This tutorial will cover the concept of numberic types in Python3.x
# There are three numeric types in Python
# 1. Int
# 2. Float
# 3. Complex

# Int
positive_int = 55
negative_int = -1039
zero = 0
print(positive_int)
print(negative_int)
print(zero)
print(type(negative_int))

# Float
positive_float = 1.497
negative_float = -2.9987654
exponent_float = 3e8 # e to indicate the power of 10

print(positive_float)
print(negative_float)
print(exponent_float)
print(type(exponent_float))

# Complex 
# Number like 3 + 5j where j represents an imaginary part
complex_num = -5 + 2j
print(complex_num)
print(type(complex_num))

